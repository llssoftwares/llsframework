namespace LLSFramework.Core.Pagination;

/// <summary>
/// Specifies the direction in which to sort a collection.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Sort in ascending order (e.g., A to Z, 0 to 9).
    /// </summary>
    Ascending,

    /// <summary>
    /// Sort in descending order (e.g., Z to A, 9 to 0).
    /// </summary>
    Descending
}
